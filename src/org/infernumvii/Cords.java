package org.infernumvii;

public class Cords {
    private int x;
    private int y;
    private int R;

    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public int getR() {
        return R;
    }
    
    @Override
    public String toString() {
        return "Cords [x=" + x + ", y=" + y + ", R=" + R + "]";
    }
    
}
